var n1 = window.prompt("POR FAVOR, DIGITE UM NÚMERO:")      /*armazenando o primeiro número*/
var n2 = window.prompt("POR FAVOR, DIGITE OUTRO NÚMERO")    /*armazenando o segundo número*/
var soma = n1 + n2                                          /*operação aritmética entre as variáveis com resultado em uma nova variável*/
window.alert("Sua soma é = " + soma)                        /*exibindo o resultado da manipulação dos dados armazenados*/ 

